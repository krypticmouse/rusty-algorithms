    let a = 10;
    let b = 2;

    println!("a + b => {}", a + b);
    println!("a - b => {}", a - b);
    println!("a * b => {}", a * b);
    println!("a / b => {}", a / b);
    println!("a % b => {}", a % b);

    println!("a & b => {}", a & b);
    println!("a | b => {}", a | b);
    println!("a ^ b => {}", a ^ b);
    println!("a << b => {}", a << b);
    println!("a >> b => {}", a >> b);

    println!("a == b => {}", a == b);
    println!("a != b => {}", a != b);
    println!("a > b => {}", a > b);
    println!("a < b => {}", a < b);
    println!("a >= b => {}", a >= b);
    println!("a <= b => {}", a <= b);
